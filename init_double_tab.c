void	init_double_tab(char **tab, int size, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (j < size)
	{
		while (i < size)
		{
			tab[j][i] = c;
			i++;
		}
		i = 0;
		j++;
	}
}
